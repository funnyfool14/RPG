package character;

import java.util.ArrayList;

public abstract class Character {

    private double hp;
    private double maxHp;
    private double mp;
    private double maxMp;
    private double offence;
    private double defence;
    private double buffOffence;
    private double buffDefence;
    private double agility;
    private int level;
    private int exp;

    private String name;
    private String type;
    private char ch;

    private ArrayList<Character> characters;

    public Character(String name, ArrayList<Character> characters){ // Humanタイプのインスタンス

        this.name = name;
        characters.add(this);
    }

    public Character(){ // Monsterタイプのインスタンス

    }

    public double getHp() {
        return hp;
    }

    public void setHp(double hp) {
        this.hp = hp;
    }

    public double getMaxHp() {
        return maxHp;
    }

    public void setMaxHp(double maxHp) {
        this.maxHp = maxHp;
    }

    public double getMp() {
        return mp;
    }

    public void setMp(double mp) {
        this.mp = mp;
    }

    public double getMaxMp() {
        return maxMp;
    }

    public void setMaxMp(double maxMp) {
        this.maxMp = maxMp;
    }

    public double getOffence() {
        return offence;
    }

    public void setOffence(double offence) {
        this.offence = offence;
    }

    public double getDefence() {
        return defence;
    }

    public void setDefence(double defence) {
        this.defence = defence;
    }

    public double getBuffOffence() {
        return buffOffence;
    }

    public void setBuffOffence(double buffOffence) {
        this.buffOffence = buffOffence;
    }

    public double getBuffDefence() {
        return buffDefence;
    }

    public void setBuffDefence(double buffDefence) {
        this.buffDefence = buffDefence;
    }

    public double getAgility() {
        return agility;
    }

    public void setAgility(double agility) {
        this.agility = agility;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public int getExp() {
        return exp;
    }

    public void setExp(int exp) {
        this.exp = exp;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public char getCh() {
        return ch;
    }

    public void setCh(char ch) {
        this.ch = ch;
    }
}
